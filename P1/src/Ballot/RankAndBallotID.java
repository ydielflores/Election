package Ballot;

public class RankAndBallotID {
	
	int ballotID;
	int rank;
	
	public RankAndBallotID(int ballotID, int rank) {
		this.ballotID = ballotID;
		this.rank = rank;
	}

	public int getBallotID() {
		return ballotID;
	}

	public void setBallotID(int ballotID) {
		this.ballotID = ballotID;
	}

	public int getRank() {
		return rank;
	}

	public void setRank(int rank) {
		this.rank = rank;
	}
	
	
	
	
}
